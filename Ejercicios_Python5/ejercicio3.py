nume_dia = int(input("Ingrese un número del 1 al 7: "))

if nume_dia == 1:
    print("hoy es Lunes")
elif nume_dia == 2:
    print("hoy es Martes")
elif nume_dia == 3:
    print("hoy es Miercoles") 
elif nume_dia == 4:
    print("hoy es Jueves")
elif nume_dia == 5:
    print("hoy es Viernes") 
elif nume_dia == 6:
    print("hoy es Sabado") 
elif nume_dia == 7:
    print("hoy es Domingo")
else:
    print("Su número no esta en el rango establecido")